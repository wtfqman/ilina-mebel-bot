const { CUSTOMER_MENU_BUTTONS, COMMON_BUTTONS, DESIGNER_MENU_BUTTONS } = require('../constants/buttons');
const { USER_ROLES } = require('../constants/user-roles');
const { buildReplyKeyboard } = require('./build-reply-keyboard');

const MAIN_MENU_ROWS = Object.freeze({
  [USER_ROLES.CUSTOMER]: [
    [CUSTOMER_MENU_BUTTONS.FAQ, CUSTOMER_MENU_BUTTONS.CATALOGS],
    [CUSTOMER_MENU_BUTTONS.PRICE, CUSTOMER_MENU_BUTTONS.LOCATION],
    [CUSTOMER_MENU_BUTTONS.MANAGERS, CUSTOMER_MENU_BUTTONS.REQUEST],
    [CUSTOMER_MENU_BUTTONS.CONSULTATION],
    [COMMON_BUTTONS.CHANGE_ROLE],
  ],
  [USER_ROLES.DESIGNER]: [
    [DESIGNER_MENU_BUTTONS.CATALOGS, DESIGNER_MENU_BUTTONS.MODELS_3D],
    [DESIGNER_MENU_BUTTONS.COOPERATION, DESIGNER_MENU_BUTTONS.PROJECT_REQUEST],
    [DESIGNER_MENU_BUTTONS.CONSULTATION, DESIGNER_MENU_BUTTONS.LOCATION],
    [COMMON_BUTTONS.CHANGE_ROLE],
  ],
});

function getMainMenuKeyboard(role) {
  return buildReplyKeyboard(MAIN_MENU_ROWS[role] || [[COMMON_BUTTONS.CHANGE_ROLE]]);
}

module.exports = {
  getMainMenuKeyboard,
};
