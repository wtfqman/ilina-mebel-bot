const { USER_ROLE_LABELS } = require('./user-roles');

const BOT_TEXTS = {
  start(botName) {
    const introLine = botName
      ? `Я ${botName} и помогу быстро найти нужный раздел, каталог или форму обращения.`
      : 'Я бот мебельной компании и помогу быстро перейти в нужный раздел.';

    return [
      'Здравствуйте!',
      introLine,
      'Для начала выберите, кто вы: заказчик или дизайнер.',
    ].join('\n\n');
  },

  chooseRole:
    'Выберите роль ниже. После этого я открою персональное меню с подходящими разделами.',

  roleChanged:
    'Роль сброшена. Выберите новый сценарий работы, и я перестрою меню под него.',

  roleMenu(role) {
    const roleLabel = USER_ROLE_LABELS[role] || 'пользователя';

    return `Главное меню для роли "${roleLabel}". Выберите нужный раздел.`;
  },

  roleSelected(role) {
    const roleLabel = USER_ROLE_LABELS[role] || 'пользователя';
    return `Роль "${roleLabel}" сохранена. Открываю главное меню.`;
  },

  roleRequired:
    'Сначала выберите роль, чтобы я показал нужные разделы и корректно сохранил дальнейшие заявки.',

  accessDenied:
    'Этот раздел сейчас недоступен для выбранной роли. Ниже открыто актуальное меню.',

  unknownWithRole:
    'Используйте кнопки ниже, чтобы открыть нужный раздел или вернуться в главное меню.',

  unknownWithoutRole:
    'Сейчас нужно выбрать роль. После этого станут доступны каталоги, формы и навигация.',

  genericError:
    'Произошла ошибка при обработке запроса. Попробуйте еще раз или вернитесь в главное меню.',
};

module.exports = {
  BOT_TEXTS,
};
